     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit(Request $request, $id)
    {
        $request->validate([
            'credits' => ['required']
        ]);

        $seller = Seller::findOrFail($id);
        $seller->credits = $seller->credits + $request->credits;

        if ($seller->save()) {
            return new SellerResource($seller);
        }
    }

